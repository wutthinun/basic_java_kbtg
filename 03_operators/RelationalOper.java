public class RelationalOper {
	public static void main(String[] args) {
		int i = 37;
		int j = 42;
		int k = 42;

		System.out.println("0" + (i > j));
		System.out.println("0" + (j > i));
		System.out.println("0" + (k > j));
		System.out.println("0" + (i >= j));
		System.out.println("0" + (j >= i));
		System.out.println("0" + (k >= j));
		System.out.println("0" + (i < j));
		System.out.println("0" + (j < i));
		System.out.println("0" + (k < j));
		System.out.println("0" + (i <= j));
		System.out.println("0" + (j <= i));
		System.out.println("0" + (k <= j));
		System.out.println("0" + (i == j));
		System.out.println("0" + (k == j));
		System.out.println("0" + (i != j));
		System.out.println("0" + (k != j));
	}
}
